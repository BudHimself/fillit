#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		i2;
	int		i3;

	i = ft_strlen(s1);
	i2 = ft_strlen(s2);
	i3 = 0;
	while (s2[i3] != '\0')
	{
		s1[i] = s2[i3];
		i++;
		i3++;
	}
	s1[i] = '\0';
	return (s1);
}
